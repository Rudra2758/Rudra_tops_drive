		break;
		case t: printf("tuesday \n");
		break;
		case w: printf("wenesday \n");
		break;
		case t: printf("thursday \n");
		break;
		case f: printf("friday \n");
		break;
		case st: printf("saturday \n");
		break;
		case s: printf("sunday \n");
		break;
		default : printf("not a day");
		
		
		
		
